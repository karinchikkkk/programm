def derivative(function, x):
    """
    Вычисляет производную функции в точке x.

    Args:
        function (str): Строковое представление функции.
        x (float): Точка, в которой вычисляется производная.

    Returns:
        float: Значение производной в точке x.
    """

    # Преобразуем строковое представление функции в выражение Python.
    expr = eval(function)

    # Вычисляем значение функции в точке x и в точке x + h.
    f_x = expr.subs(x, x)
    f_x_plus_h = expr.subs(x, x + h)

    # Вычисляем производную как предел отношения разности значений функции к разности аргументов при h, стремящемся к нулю.
    return (f_x_plus_h - f_x) / h

# Запрашиваем у пользователя строковое представление функции и точку, в которой нужно вычислить производную.
function = input("Введите строковое представление функции: ")
x = float(input("Введите точку, в которой нужно вычислить производную: "))

# Вычисляем производную и выводим ее на экран.
result = derivative(function, x)
print(f"Производная функции {function} в точке {x} равна {result}")
